public class Student {
    private String Name;
    private int Age;
    private String Major;

    public Student() {

    }

    public String getName() {
        return this.Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }
    public int getAge() {
        return this.Age;
    }

    public void setAge(int Age) {
        this.Age = Age;
    }
    public String getMajor() {
        return this.Major;
    }

    public void setMajor(String Major) {
        this.Major = Major;
    }
}
